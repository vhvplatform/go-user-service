// Helper functions to generate more users
export function generateMockUser(id: string, index: number): User {
  const roles = mockRoles;
  const statuses: User['status'][] = ['active', 'inactive', 'suspended'];
  const randomRole = roles[Math.floor(Math.random() * roles.length)];
  const randomStatus = statuses[Math.floor(Math.random() * statuses.length)];
  
  const firstName = `User${index}`;
  const lastName = `Test${index}`;
  const username = `user${index}`;
  
  return {
    id,
    email: `user${index}@vhvplatform.com`,
    username,
    firstName,
    lastName,
    phoneNumber: `+8490${1000000 + index}`,
    avatar: `https://api.dicebear.com/7.x/avataaars/svg?seed=user${index}`,
    status: randomStatus,
    roleId: randomRole.id,
    role: randomRole,
    lastLoginAt: new Date(Date.now() - Math.random() * 30 * 24 * 60 * 60 * 1000).toISOString(),
    loginCount: Math.floor(Math.random() * 1000),
    failedAttempts: Math.floor(Math.random() * 5),
    createdAt: new Date(Date.now() - Math.random() * 365 * 24 * 60 * 60 * 1000).toISOString(),
    updatedAt: new Date().toISOString(),
  };
}

export function generateMockUsers(count: number): User[] {
  const users: User[] = [...mockUsers];
  for (let i = mockUsers.length; i < count; i++) {
    users.push(generateMockUser(String(i + 1), i + 1));
  }
  return users;
}
